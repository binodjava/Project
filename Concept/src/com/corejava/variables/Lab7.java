package com.corejava.variables;

public class Lab7 {
	public static void main(String[] args) {
		int a = 99;
		boolean b = true;
		double d = 999.99;
		String str = "BK";
		System.out.println(a);// 99
		System.out.println(b);// true
		System.out.println(d);// 999.99
		System.out.println(str);// BK

		/*
		 * We can initialize variables in two ways. 
		 * 1. Declare and then initialize it 
		 * like int a; 
		 * a=99; 
		 * 2. Declare and initialize in single
		 * line like int a=99;
		 */

	}

}
