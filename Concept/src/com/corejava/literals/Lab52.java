package com.corejava.literals;

public class Lab52 {
	public static void main(String[] args) {
		int a = 101;//Decimal
		int b = 0b101;//Binary "0b" denotes it is binary literals
//		[1*2^2+1*2^0]
		
//		int a1=ob102; //binary i.e 0 and 1 only
		
		
		
		System.out.println(a);
		System.out.println(b);
		
		
		int a2=101;//Decimal
		int a3=0101;//Octal
		int a4=0x101;//Hexadecimal
		int a5=0B101;//Binary
		System.out.println(a2);//101
		System.out.println(a3);//65
		System.out.println(a4);//257
		System.out.println(a5);//5
		
		
		
		
		
		

	}

}
