package com.corejava.operators;

public class Lab77 {
	public static void main(String[] args) {
		final byte b1 = 90;
		final byte b2 = 9;
		byte b3 = b1 + b2;
		System.out.println(b1);//90
		System.out.println(b2);//9
		System.out.println(b3);//99
		
		
//		final var is just like constant, we can do modification on constant data type won't be change
//		but non-final var if we do any operation result type would be larger one
		
		
	}

}
