package com.corejava.operators;

public class Lab122 {
	public static void main(String[] args) {
		int a = 10;
		int b = a++;
		System.out.println(a);//11
		System.out.println(b);//10
		System.out.println();
		int x = 10;
		int y = ++x;
		System.out.println(x);//11
		System.out.println(y);//11

	}

}
