package com.corejava.operators;

public class Lab131 {
	public static void main(String[] args) {

		System.out.println(10 > 20);//false
		System.out.println(10 < 20);//true
		System.out.println(10 >= 20);//false
		System.out.println(10 <= 20);//true
		System.out.println(10 <= 10);//true
		System.out.println(10 >= 10);//true
		System.out.println(10 == 10);//true
		System.out.println(10 == 12);//false
	}

}
